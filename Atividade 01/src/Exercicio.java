public class Exercicio {
   public static void main(String[] args) {
    //inteoros 
    int x = 40;
    int y = 10;
    //soma
    System.out.println(x+y);
    //Multiplicação
    System.out.println(x*y);
    //Divisão
    System.out.println(x/y);
    //Resto da divisão
    System.out.println(x%y);
    //float
    float a = 4.5f;
    float b = 2.5f;
    //soma
    System.out.println(a+b);
    //subtração 
    System.out.println(a - b);
    //Double
    double c = 4.55;
    double d = 2.55;
    //Divisão
    System.out.println(c/d);
    //Multiplicação
    System.out.println(c*d);
    
   }
    
}
