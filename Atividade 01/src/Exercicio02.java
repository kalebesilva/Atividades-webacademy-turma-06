import java.util.*;
public class Exercicio02 {
    public static void main(String[] args) throws Exception {
        /**
         *Metodos de entrtada de dados
         */
        Scanner entrada = new Scanner(System.in);
        // entando com os dados
        int x = entrada.nextInt();
        int y = entrada.nextInt();
        //soma
        System.out.println("Valor da Soma: "+(x+y));
        //Multiplicação
        System.out.println("Valor da Multiplicação: "+(x*y));
        //Divisão
        System.out.println("Valor da Divisão: "+(x/y));
        //subtração
        //int a = x-y;
        System.out.println("valor da Subtração: " + (x-y));
    }
    

}

